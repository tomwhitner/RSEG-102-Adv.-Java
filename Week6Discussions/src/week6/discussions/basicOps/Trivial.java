package week6.discussions.basicOps;

public class Trivial {

	public static void main(String args[]) {

		System.out.print("H" + "a");

		System.out.print('H' + 'a');

		int H = 'H';
		int a = 'a';
		System.out.println("H = " + H);
		System.out.println("a = " + a);
		System.out.println("H + a = " + (H+a));
		
	}
	

}
